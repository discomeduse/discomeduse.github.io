////////////////////////////////////////////////////////////////////////////////
// Twitter
var qnibru = document.querySelector('.qnibru');
var Twitter = document.querySelector('.Twitter');

Twitter.addEventListener('mouseover', function () {
	this.classList.add('ColorTwitter');
  nibru.classList.add('ColorTwitter');
});

Twitter.addEventListener('mouseout', function () {
	this.classList.remove('ColorTwitter');
  nibru.classList.remove('ColorTwitter');
});

